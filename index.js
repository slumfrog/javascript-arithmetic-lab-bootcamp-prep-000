
function add(a, b){
  number = a + b;
  return number;
}

function subtract(a, b){
  number = a - b;
  return number;
}


function multiply(a, b){
  number = a * b;
  return number;
}

function divide(a, b){
  number = a / b;
  return number;
}

function inc(a){
  a += 1;
  return a;
}

function dec(a){
  a -= 1;
  return a;
}